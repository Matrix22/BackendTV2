package datafetch;

public final class SortFilterFetch {
    private String rating;
    private String duration;

    public SortFilterFetch() {

    }

    public String getRating() {
        return rating;
    }

    public void setRating(final String newRating) {
        rating = newRating;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(final String newDuration) {
        duration = newDuration;
    }
}
